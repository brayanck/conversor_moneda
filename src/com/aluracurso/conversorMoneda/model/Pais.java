package com.aluracurso.conversorMoneda.model;

public class Pais {
        private String base;
        private String pais;

        public Pais(String base, String pais) {
            this.base = base;
            this.pais = pais;
        }

        public String getBase() {
            return base;
        }

        public String getPais() {
            return pais;
        }

}
